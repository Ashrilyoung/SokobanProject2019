package sokobandriver;

/**
 *
 * @author 16007873
 * class to set coordinates for the objects
 */

public class Coordinate {

    private int x = 0;
    private int y = 0;

    /**
     *
     * @return
     */
    public int getX() {
        return x;
    }

    /**
     *
     * @return
     */
    public int getY() {
        return y;
    }

    /**
     *
     * @param newX
     */
    public void setX(int newX) {
        x = newX;
    }

    /**
     *
     * @param newY
     */
    public void setY(int newY) {
        y = newY;
    }

}
